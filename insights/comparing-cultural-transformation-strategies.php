                                            <li><strong>Direct Impact:</strong> Leaders had a more direct impact on smaller teams, making it easier to model desired behaviors and drive cultural change.</li>
                                        </ul>
                                        <p><strong>GFS:</strong></p>
                                        <ul>
                                            <li><strong>Extensive Leadership Programs:</strong> Required extensive and ongoing leadership training programs to ensure leaders across all levels and regions were aligned with the cultural transformation goals.</li>
                                            <li><strong>Distributed Leadership:</strong> Leaders needed to manage larger, more dispersed teams, requiring advanced skills in remote team management and cross-cultural communication.</li>
                                        </ul>

