                                        <p><strong>Tech Innovators Inc.:</strong></p>
                                        <ul>
                                            <li><strong>Inclusive Culture:</strong> Focused on embedding inclusion into the culture strategy with regular DEI training and employee resource groups.</li>
                                            <li><strong>Cultural Connectedness:</strong> Addressed cultural connectedness for underrepresented groups, leading to a stronger sense of belonging and engagement.</li>
